def area(width, height):
    result = width * height
    return result

def subtract(num1, num2):
    result = num1 - num2
    return result

def divide(num1, num2):
    result = num1 / num2
    return result

def add(num1,num2):
    result = num1 + num2
    return result
    
def multiply(num1,num2):
    result = num1 * num2
    return result
    

